package Immutable;

public class Pureimmutable {
        private final int id;
		private final String name;
		
		Pureimmutable(int id,String name){
			this.name=name;
			this.id=id;
		}
	    public int getid()
		  {
			return id;
		      }
		 public String getname()
		    {
			return name;
		      }
		

	}


